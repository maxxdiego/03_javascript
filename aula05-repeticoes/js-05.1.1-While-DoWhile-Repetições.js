console.log('Repetição com While')
var a = 5
var b = 12
var p = 2

while (a <= b) {
    console.log(`Passo ${a}`)
    a+=p
}

console.log('Repetição com Do While')
var a = 5
var b = 12
var p = 2

do {
    console.log(`Passo ${a}`)
    a += p
} while (a <= b)

